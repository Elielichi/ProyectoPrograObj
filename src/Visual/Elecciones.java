    DefaultTableModel modeloTabla;
    public void inicializarTabla(){
        modeloTabla = new DefaultTableModel();
        this.tblEleccion.setModel(modeloTabla);
        modeloTabla.addColumn("Tipo de Eleccion");
        modeloTabla.addColumn("Fecha");
        modeloTabla.addColumn("Nombre");
    }
    
    private void mostrarEleccionesTabla(){
        inicializarTabla();
        LinkedList<Eleccion> listaElecciones = this.controlador.getListaElecciones();
        for(Eleccion eleccion: listaElecciones){
            String [] fila = new String[4];
            fila[0] = eleccion.getTipo();
            fila[1] = eleccion.getFecha();
            fila[2] = eleccion.getNombre();
            this.modeloTabla.addRow(fila);
        }
    }

