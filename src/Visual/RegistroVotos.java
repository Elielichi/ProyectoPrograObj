    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String nombreCandidato = this.cbxCandidato.getSelectedItem().toString();
        Candidato candidato = controladorCandidato.buscarCandidatoPorNombre(nombreCandidato);
        String numeroActa = this.cbxNumeroActa.getSelectedItem().toString();
        
        ActaElectoral acta = controladorActa.buscarActaPorNumActa(numeroActa);
        int votosEfectivos = (Integer)sprEfectivos.getValue();
        int votosBlancos = (Integer)sprBlancos.getValue();
        int votosNulos = (Integer)sprNulos.getValue();
        
        RegistroVoto registro = new RegistroVoto(acta,candidato,votosEfectivos,votosNulos,votosBlancos);
        boolean resultado = controlador.agregarRegistroVoto(registro);
        if (resultado) {
            JOptionPane.showMessageDialog(this, "Registro agregado correctamente.", "", JOptionPane.INFORMATION_MESSAGE);
            mostrarRegistrosVotosTabla(); // Mostrar los datos al instante
            limpiar(); // Limpiar los campos
        } else {
            JOptionPane.showMessageDialog(this, "Error al agregar el registro.", "", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String numeroActa = cbxNumeroActa.getSelectedItem().toString();
        String nombreCandidato = cbxCandidato.getSelectedItem().toString();

        boolean resultado = controlador.eliminarRegistroPorActaYCandidato(numeroActa, nombreCandidato);
        if (resultado) {
            JOptionPane.showMessageDialog(this, "Se elimino correctamente la informacion", "", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Hubo un error en el proceso de eliminacion.", "", JOptionPane.ERROR_MESSAGE);
        }

        limpiar();
        mostrarRegistrosVotosTabla();
    }//GEN-LAST:event_btnEliminarActionPerformed

