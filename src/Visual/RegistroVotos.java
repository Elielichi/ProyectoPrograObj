    private void llenarComboCandidatos(){
        modeloComboCandidatos = new DefaultComboBoxModel<>();
        this.cbxCandidato.setModel(modeloComboCandidatos);
        LinkedList<Candidato> lista = controladorCandidato.getListaCandidatos();
        for (Candidato candidato : lista) {
            modeloComboCandidatos.addElement(candidato.getNombre());
        }
    }
    
    private void llenarComboNumActa(){
        modeloComboNumActa = new DefaultComboBoxModel<>();
        this.cbxNumeroActa.setModel(modeloComboNumActa);
        LinkedList<ActaElectoral> lista = controladorActa.getListaActas();
        for (ActaElectoral acta : lista) {
            modeloComboNumActa.addElement(acta.getNumeroActa());
        }
    }
    
    DefaultTableModel modeloTabla;
    public void inicializarTabla(){
        modeloTabla = new DefaultTableModel();
        this.tblRegistroVotos.setModel(modeloTabla);
        modeloTabla.addColumn("Numero de acta");
        modeloTabla.addColumn("Candidato");
        modeloTabla.addColumn("Votos efectivos");
        modeloTabla.addColumn("Votos nulos");
        modeloTabla.addColumn("Votos blancos");
    }
    
    private void mostrarRegistrosVotosTabla() {
        inicializarTabla(); 
        LinkedList<RegistroVoto> lista = controlador.getListaRegistroVotos();

        for (RegistroVoto registrovoto : lista) {
            String[] fila = new String[5];
            fila[0] = registrovoto.getActa().getNumeroActa();
            fila[1] = registrovoto.getCandidato().getNombre();
            fila[2] = String.valueOf(registrovoto.getVotosValidos());
            fila[3] = String.valueOf(registrovoto.getVotosNulos());
            fila[4] = String.valueOf(registrovoto.getVotosBlancos());

            modeloTabla.addRow(fila);
        }
    }
    
    

