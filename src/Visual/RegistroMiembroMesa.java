    DefaultTableModel modeloTabla;
    public void inicializarTabla(){
        modeloTabla = new DefaultTableModel();
        this.tblMiembrosMesa.setModel(modeloTabla);
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Apellidos");
        modeloTabla.addColumn("Rol");
    }
    
    public void limpiar(){
        this.txtNombre.setText("");
        this.txtApellido.setText("");
        this.cbxRol.setSelectedIndex(0);
        
    }
    
    private boolean validarDatos(){
        if(this.txtNombre.getText().isEmpty() || this.txtApellido.getText().isEmpty()){
            return false;
        }
        return true;
    }
    
    private void mostrarMiembrosTabla(){
        inicializarTabla();
        LinkedList<MiembroMesa> listaMiembros = this.controlador.getListaMiembros();
        for(MiembroMesa miembro: listaMiembros){
            String [] fila = new String[3];
            fila[0] = miembro.getNombre();
            fila[1] = miembro.getApellidos();
            fila[2] = miembro.getRol();
            
            
            this.modeloTabla.addRow(fila);
        }
    }

