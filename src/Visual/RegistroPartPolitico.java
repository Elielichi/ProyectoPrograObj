    DefaultTableModel modeloTabla;
    public void inicializarTabla(){
        modeloTabla = new DefaultTableModel();
        this.tblPartidos.setModel(modeloTabla);
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Sigla");
        modeloTabla.addColumn("Representante");
    }
    
    private void mostrarPartidosTabla(){
        inicializarTabla();
        LinkedList<PartidoPolitico> listaPartidos = this.controlador.getListaPartidos();
        for(PartidoPolitico partido: listaPartidos){
            String [] fila = new String[3];
            fila[0] = partido.getNombre();
            fila[1] = partido.getSigla();
            fila[2] = partido.getRepresentante();
            
            this.modeloTabla.addRow(fila);
        }
    }
    
    
    private boolean validarDatos(){
        if(this.txtNombre.getText().isEmpty() || this.txtRepresentante.getText().isEmpty() || this.txtSigla.getText().isEmpty()){
            return false;
        }
        return true;
    }
    public void limpiar(){
        this.txtNombre.setText("");
        this.txtRepresentante.setText("");
        this.txtSigla.setText("");
        
    }
